package com.project.meishixing.utils;

import com.project.meishixing.R;

public class NearResBG {

	public static int setStarBG(int id) {
		if (id == 1) {
			return R.drawable.meishi1;
		} else if (id == 2) {
			return R.drawable.meishi2;
		} else if (id == 3) {
			return R.drawable.meishi3;
		} else if (id == 4) {
			return R.drawable.meishi4;
		} else if (id == 5) {
			return R.drawable.meishi5;
		} else if (id == 6) {
			return R.drawable.meishi6;
		} else if (id == 7) {
			return R.drawable.meishi7;
		} else if (id == 8) {
			return R.drawable.meishi8;
		} else if (id == 9) {
			return R.drawable.meishi9;
		}
		return R.drawable.icon;

	}
}
